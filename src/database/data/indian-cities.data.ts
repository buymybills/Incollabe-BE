/**
 * Helper function to get tier classification for a city
 */
export function getCityTier(cityName: string): 1 | 2 | 3 {
  const normalizedName = cityName.trim();

  if (tier1Cities.includes(normalizedName)) {
    return 1;
  }

  if (tier2Cities.includes(normalizedName)) {
    return 2;
  }

  return 3;
}

/**
 * Returns all Indian cities with proper tier classification
 */
export function getAllIndianCities(): IndianCityData[] {
  return indianCities.map((city) => ({
    ...city,
    tier: city.tier || getCityTier(city.name),
  }));
}
